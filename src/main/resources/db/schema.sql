-- Reuse POS item management schema (H2 Oracle-compatible)

CREATE TABLE IF NOT EXISTS ITEM_CATEGORY (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR(50) NOT NULL UNIQUE,
    DESCRIPTION VARCHAR(200)
);

CREATE TABLE IF NOT EXISTS BUYBACK_ITEM (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ITEM_CODE VARCHAR(20) NOT NULL UNIQUE,
    NAME VARCHAR(100) NOT NULL,
    CATEGORY_ID BIGINT NOT NULL,
    PURCHASE_PRICE DECIMAL(10,0) NOT NULL,
    SELLING_PRICE DECIMAL(10,0),
    ITEM_CONDITION VARCHAR(10) NOT NULL DEFAULT 'B',
    STATUS VARCHAR(20) NOT NULL DEFAULT 'IN_STOCK',
    CUSTOMER_NAME VARCHAR(100),
    PURCHASED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_CATEGORY FOREIGN KEY (CATEGORY_ID) REFERENCES ITEM_CATEGORY(ID),
    CONSTRAINT CHK_CONDITION CHECK (ITEM_CONDITION IN ('S', 'A', 'B', 'C', 'D')),
    CONSTRAINT CHK_STATUS CHECK (STATUS IN ('IN_STOCK', 'ON_DISPLAY', 'SOLD', 'RETURNED'))
);
